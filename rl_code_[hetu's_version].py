def select_action(state, eps):
    if random.random() < eps:
        return random.randrange(num_actions)
    with torch.no_grad():
        s = torch.tensor(state, dtype=torch.float32, device=torch_device).unsqueeze(0)
        q = policy_net(s)
        return int(torch.argmax(q, dim=1).item())

def optimize_model():
    if len(buffer) < 2000:
        return None

    states, actions, rewards, next_states, dones = buffer.sample(batch_size)

    states_t = torch.tensor(states, dtype=torch.float32, device=torch_device)
    actions_t = torch.tensor(actions, dtype=torch.int64, device=torch_device).unsqueeze(1)
    rewards_t = torch.tensor(rewards, dtype=torch.float32, device=torch_device).unsqueeze(1)
    next_states_t = torch.tensor(next_states, dtype=torch.float32, device=torch_device)
    dones_t = torch.tensor(dones, dtype=torch.float32, device=torch_device).unsqueeze(1)

    current_q = policy_net(states_t).gather(1, actions_t)

    with torch.no_grad():
      next_actions = policy_net(next_states_t).argmax(dim=1, keepdim=True)
      next_q = target_net(next_states_t).gather(1, next_actions)
      target_q = rewards_t + gamma * next_q * (1.0 - dones_t)

    loss = loss_fn(current_q, target_q)

    optimizer.zero_grad()
    loss.backward()
    torch.nn.utils.clip_grad_norm_(policy_net.parameters(), max_norm=10.0)  # prevent exploding gradients
    optimizer.step()

    return float(loss.item())

