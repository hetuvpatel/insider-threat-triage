class ReplayBuffer:
    def __init__(self, capacity=50000):
        self.normal_buffer   = deque(maxlen=capacity)
        self.malicious_buffer = deque(maxlen=capacity)

    def push(self, state, action, reward, next_state, done, true_label):
        transition = (state, action, reward, next_state, done, true_label)
        if true_label == 1:
            self.malicious_buffer.append(transition)
        else:
            self.normal_buffer.append(transition)

    def sample(self, batch_size, mal_ratio=0.50):
        """Guarantee mal_ratio of every batch is malicious transitions."""
        n_mal  = min(len(self.malicious_buffer), int(batch_size * mal_ratio))
        n_norm = min(len(self.normal_buffer), batch_size - n_mal)
        # Fill remainder if one side is short
        if n_mal + n_norm < batch_size:
            extra = min(len(self.normal_buffer) - n_norm, batch_size - n_mal - n_norm)
            n_norm += max(extra, 0)

        mal_batch  = random.sample(list(self.malicious_buffer), n_mal)  if n_mal  > 0 else []
        norm_batch = random.sample(list(self.normal_buffer),    n_norm) if n_norm > 0 else []

        batch = mal_batch + norm_batch
        random.shuffle(batch)
        s, a, r, ns, d, _ = zip(*batch)

        return (
            np.array(s,  dtype=np.float32),
            np.array(a,  dtype=np.int64),
            np.array(r,  dtype=np.float32),
            np.array(ns, dtype=np.float32),
            np.array(d,  dtype=np.float32),
        )

    def __len__(self):
        return len(self.normal_buffer) + len(self.malicious_buffer)

    def counts(self):
        return {"normal": len(self.normal_buffer), "malicious": len(self.malicious_buffer), "total": len(self)}

