# ---------------------------------
# Action selection
# ---------------------------------
def sarsa_select_action(state, eps):
    if random.random() < eps:
        return random.randrange(num_actions)

    with torch.no_grad():
        s = torch.tensor(state, dtype=torch.float32, device=torch_device).unsqueeze(0)
        q_values = sarsa_net(s)
        return int(torch.argmax(q_values, dim=1).item())

# ---------------------------------
# Single SARSA update
# ---------------------------------
def sarsa_update(state, action, reward, next_state, next_action, done):
    """
    SARSA target:
        target = r + gamma * Q(s', a')   if not done
               = r                       if done
    """
    state_t = torch.tensor(state, dtype=torch.float32, device=torch_device).unsqueeze(0)
    next_state_t = torch.tensor(next_state, dtype=torch.float32, device=torch_device).unsqueeze(0)

    action_t = torch.tensor([[action]], dtype=torch.int64, device=torch_device)
    reward_t = torch.tensor([[reward]], dtype=torch.float32, device=torch_device)

    current_q = sarsa_net(state_t).gather(1, action_t)

    with torch.no_grad():
        if done:
            target_q = reward_t
        else:
            next_action_t = torch.tensor([[next_action]], dtype=torch.int64, device=torch_device)
            next_q = sarsa_net(next_state_t).gather(1, next_action_t)
            target_q = reward_t + gamma * next_q

    loss = loss_fn(current_q, target_q)

    optimizer.zero_grad()
    loss.backward()
    torch.nn.utils.clip_grad_norm_(sarsa_net.parameters(), max_norm=10.0)
    optimizer.step()

    return float(loss.item())

# ---------------------------------
# Detailed policy evaluation
# ---------------------------------
def evaluate_sarsa_policy(env, num_eval_episodes=None, eps=0.0):
    """
    Runs greedy evaluation by default (eps=0.0) and returns:
    1) step-level dataframe
    2) episode-level dataframe
    """
    if num_eval_episodes is None:
        num_eval_episodes = len(env.episodes)

    step_rows = []
    episode_rows = []

    for ep_idx in range(num_eval_episodes):
        state, info = env.reset()
        done = False

        ep_reward = 0.0
        ep_len = 0
        ep_actions = []
        ep_labels = []
        ep_risks = []

        while not done:
            action = sarsa_select_action(state, eps=eps)
            next_state, reward, terminated, truncated, info = env.step(action)
            done = terminated or truncated

            ep_reward += reward
            ep_len += 1
            ep_actions.append(action)
            ep_labels.append(info["true_label"])
            ep_risks.append(info["risk_score"])

            step_rows.append({
                "episode_id": ep_idx,
                "user": info["user"],
                "time": info["time"],
                "true_label": int(info["true_label"]),
                "action": int(action),
                "reward": float(reward),
                "risk_score": float(info["risk_score"]),
                "after_hours": float(info["after_hours"]),
                "usb_used": float(info["usb_used"]),
                "external_email_burst": float(info["external_email_burst"]),
                "file_burst": float(info["file_burst"]),
                "current_risk": float(info["current_risk"]),
                "done": int(done),
            })

            state = next_state

        episode_rows.append({
            "episode_id": ep_idx,
            "user": step_rows[-1]["user"] if len(step_rows) > 0 else None,
            "episode_length": ep_len,
            "episode_reward": ep_reward,
            "malicious_steps": int(np.sum(ep_labels)),
            "normal_steps": int(len(ep_labels) - np.sum(ep_labels)),
            "close_rate": float(np.mean(np.array(ep_actions) == 0)) if ep_actions else 0.0,
            "investigate_rate": float(np.mean(np.array(ep_actions) == 1)) if ep_actions else 0.0,
            "escalate_rate": float(np.mean(np.array(ep_actions) == 2)) if ep_actions else 0.0,
            "avg_risk_score": float(np.mean(ep_risks)) if ep_risks else 0.0,
            "max_risk_score": float(np.max(ep_risks)) if ep_risks else 0.0,
            "had_any_malicious": int(np.sum(ep_labels) > 0),
            "detected_any_malicious": int(
                np.any((np.array(ep_labels) == 1) & (np.array(ep_actions) >= 1))
            ) if ep_actions else 0,
            "escalated_any_malicious": int(
                np.any((np.array(ep_labels) == 1) & (np.array(ep_actions) == 2))
            ) if ep_actions else 0,
        })

    steps_df = pd.DataFrame(step_rows)
    episodes_df = pd.DataFrame(episode_rows)

    return steps_df, episodes_df

# ---------------------------------
# Metrics computation
# ---------------------------------
def compute_sarsa_metrics(results_df):
    """
    We will measure:
    - binary detection (close=0 => normal, investigate/escalate => suspicious)
    - escalation quality
    - action distribution
    - fairness / per-user behavior
    """
    results = results_df.copy()

    results["action_name"] = results["action"].map({
        0: "close",
        1: "investigate",
        2: "escalate"
    })

    # Binary detection view:
    # close -> predicted normal
    # investigate/escalate -> predicted suspicious
    y_true = results["true_label"].astype(int).values
    y_pred_binary = (results["action"] != 0).astype(int).values
    y_pred_escalate = (results["action"] == 2).astype(int).values

    metrics = {}

    # Action mix
    action_mix = results["action_name"].value_counts(normalize=True).to_dict()
    metrics["action_mix"] = action_mix

    # Main triage metrics
    malicious_rows = results[results["true_label"] == 1]
    normal_rows = results[results["true_label"] == 0]

    metrics["malicious_detect_rate"] = float((malicious_rows["action"] >= 1).mean()) if len(malicious_rows) else 0.0
    metrics["malicious_escalation_rate"] = float((malicious_rows["action"] == 2).mean()) if len(malicious_rows) else 0.0
    metrics["malicious_miss_rate"] = float((malicious_rows["action"] == 0).mean()) if len(malicious_rows) else 0.0
    metrics["normal_close_rate"] = float((normal_rows["action"] == 0).mean()) if len(normal_rows) else 0.0
    metrics["false_positive_escalation_rate"] = float((normal_rows["action"] == 2).mean()) if len(normal_rows) else 0.0
    metrics["false_positive_detect_rate"] = float((normal_rows["action"] >= 1).mean()) if len(normal_rows) else 0.0

    # Escalation precision/recall
    total_escalated = int((results["action"] == 2).sum())
    correct_escalated = int(((results["true_label"] == 1) & (results["action"] == 2)).sum())
    total_malicious = int((results["true_label"] == 1).sum())

    metrics["escalation_precision"] = correct_escalated / max(total_escalated, 1)
    metrics["escalation_recall"] = correct_escalated / max(total_malicious, 1)

    # Binary detection metrics
    metrics["binary_precision"] = float(precision_score(y_true, y_pred_binary, zero_division=0))
    metrics["binary_recall"] = float(recall_score(y_true, y_pred_binary, zero_division=0))
    metrics["binary_f1"] = float(f1_score(y_true, y_pred_binary, zero_division=0))

    # Escalation as positive class
    metrics["escalation_as_positive_precision"] = float(precision_score(y_true, y_pred_escalate, zero_division=0))
    metrics["escalation_as_positive_recall"] = float(recall_score(y_true, y_pred_escalate, zero_division=0))
    metrics["escalation_as_positive_f1"] = float(f1_score(y_true, y_pred_escalate, zero_division=0))

    # AUC using risk score if both classes present
    if len(np.unique(y_true)) == 2:
        metrics["risk_score_auc"] = float(roc_auc_score(y_true, results["risk_score"].values))
    else:
        metrics["risk_score_auc"] = np.nan

    # Confusion matrices
    metrics["binary_confusion_matrix"] = confusion_matrix(y_true, y_pred_binary).tolist()
    metrics["escalation_confusion_matrix"] = confusion_matrix(y_true, y_pred_escalate).tolist()

    # Per-user fairness style summary
    user_fairness = results.groupby("user").apply(
        lambda g: pd.Series({
            "rows": len(g),
            "malicious_rate": g["true_label"].mean(),
            "escalation_rate": (g["action"] == 2).mean(),
            "investigate_or_escalate_rate": (g["action"] != 0).mean(),
            "false_positive_rate": ((g["true_label"] == 0) & (g["action"] == 2)).mean(),
            "miss_rate": ((g["true_label"] == 1) & (g["action"] == 0)).mean(),
            "avg_risk_score": g["risk_score"].mean(),
        })
    ).reset_index()

    return metrics, user_fairness

