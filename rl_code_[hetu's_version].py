import gymnasium as gym
from gymnasium import spaces

class CertTriageEnv(gym.Env):
    """
    Sequential RL environment over CERT user timelines.
    State = current incident features + rolling risk info
    Action affects future risk, so future states depend on actions.
    """

    def __init__(self, episodes, feature_cols, max_steps=150):
        super().__init__()
        self.episodes = episodes
        self.feature_cols = feature_cols
        self.max_steps = max_steps
        self.malicious_episodes = [ep for ep in episodes if ep["label"].sum() > 0]
        self.normal_episodes    = [ep for ep in episodes if ep["label"].sum() == 0]

        self.num_base_features = len(feature_cols)
        self.state_dim = self.num_base_features + 3

        self.action_space = spaces.Discrete(3)
        self.observation_space = spaces.Box(
            low=-np.inf, high=np.inf, shape=(self.state_dim,), dtype=np.float32
        )

        self.current_episode = None
        self.t = 0
        self.steps_this_episode = 0
        self.risk_score = 0.0
        self.prev_action = 0
        self.steps_since_escalate = 999

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)

        if len(self.malicious_episodes) > 0 and np.random.rand() < 0.70:
            self.current_episode = self.malicious_episodes[
                np.random.randint(len(self.malicious_episodes))
            ]
            mal_positions = np.where(self.current_episode["label"].values == 1)[0]
            if len(mal_positions) > 0:
                first_mal = int(mal_positions[0])
                start = max(0, first_mal - np.random.randint(5, 20))
                self.t = start
            else:
                self.t = 0
        else:
            if len(self.normal_episodes) > 0:
                self.current_episode = self.normal_episodes[
                    np.random.randint(len(self.normal_episodes))
                ]
            else:
                self.current_episode = self.episodes[np.random.randint(len(self.episodes))]
            self.t = 0

        self.steps_this_episode = 0
        self.risk_score = 0.0
        self.prev_action = 0
        self.steps_since_escalate = 999
        return self._get_state(), {}

    def _base_features(self, row):
        return row[self.feature_cols].values.astype(np.float32)

    def _get_state(self):
        row = self.current_episode.iloc[self.t]
        base = self._base_features(row)
        extra = np.array([
            self.risk_score,
            float(self.prev_action) / 2.0,
            float(min(self.steps_since_escalate, 10)) / 10.0,
        ], dtype=np.float32)
        return np.concatenate([base, extra], axis=0)

    def _behavior_risk(self, row):
        risk = 0.0
        risk += 0.10 * max(0.0, float(row["file_count_z"]))
        risk += 0.05 * max(0.0, float(row["email_count_z"]))
        risk += 0.05 * max(0.0, float(row["http_count_z"]))
        risk += 0.10 * max(0.0, float(row.get("external_email_count_z", 0.0)))
        risk += 0.30 * float(row["usb_used"])
        risk += 0.10 * float(row["after_hours"])
        risk += 0.20 * float(row.get("rare_after_hours", 0.0))
        risk += 0.25 * float(row["file_burst"])
        risk += 0.10 * float(row["email_burst"])
        risk += 0.10 * float(row["http_burst"])
        risk += 0.25 * float(row.get("external_email_burst", 0.0))
        risk += 0.40 * float(row["usb_after_hours"])
        risk += 0.50 * float(row["usb_file_burst"])
        risk += 0.30 * float(row["after_hours_file_burst"])
        risk += 0.20 * float(row["after_hours_email_burst"])
        risk += 0.60 * float(row.get("usb_external_email", 0.0))
        risk += 0.40 * float(row.get("after_hours_external_email", 0.0))
        return float(np.clip(risk, 0.0, 3.0))

    def _compute_reward(self, true_label, action, current_risk):
        if true_label == 0:
            if action == 0:   return  0.0
            elif action == 1: return -0.05
            else:             return -0.60
        else:
            if action == 2:   return  1.0
            elif action == 1: return  0.30
            else:             return -1.0

    def step(self, action):
        row = self.current_episode.iloc[self.t]
        true_label   = int(row["label"])
        current_risk = self._behavior_risk(row)
        reward       = self._compute_reward(true_label, action, current_risk)

        self.risk_score = 0.65 * self.risk_score + 0.35 * current_risk
        if current_risk < 0.15:
            self.risk_score = max(0.0, self.risk_score - 0.40)
        if action == 1:
            self.risk_score *= 0.97
        elif action == 2:
            self.risk_score *= 0.45
        self.risk_score = float(np.clip(self.risk_score, 0.0, 5.0))

        self.prev_action = action
        if action == 2:
            self.steps_since_escalate = 0
        else:
            self.steps_since_escalate += 1

        self.t += 1
        self.steps_this_episode += 1
        terminated = self.t >= len(self.current_episode)
        truncated  = self.steps_this_episode >= self.max_steps

        if terminated or truncated:
            next_state = np.zeros(self.state_dim, dtype=np.float32)
        else:
            next_state = self._get_state()

        info = {
            "true_label":           true_label,
            "risk_score":           self.risk_score,
            "user":                 row["user"],
            "time":                 row["hour"],
            "after_hours":          float(row["after_hours"]),
            "usb_used":             float(row["usb_used"]),
            "external_email_burst": float(row.get("external_email_burst", 0.0)),
            "file_burst":           float(row["file_burst"]),
            "current_risk":         float(current_risk),
        }
        return next_state, reward, terminated, truncated, info

