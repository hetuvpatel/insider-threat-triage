def plot_single_episode_timeline(env, episode_index=0):
    """
    Visualize one chosen evaluation episode
    """
    chosen_episode = env.episodes[episode_index]
    single_env = CertTriageEnv([chosen_episode], feature_cols, max_steps=150)

    state, info = single_env.reset()
    done = False

    steps = []
    risks = []
    labels = []
    actions = []
    rewards = []
    times = []

    step_no = 0
    while not done:
        action = sarsa_select_action(state, eps=0.0)
        next_state, reward, terminated, truncated, info = single_env.step(action)
        done = terminated or truncated

        step_no += 1
        steps.append(step_no)
        risks.append(info["risk_score"])
        labels.append(info["true_label"])
        actions.append(action)
        rewards.append(reward)
        times.append(info["time"])

        state = next_state

    action_names = {0: "Close", 1: "Investigate", 2: "Escalate"}
    action_colors = {0: "green", 1: "orange", 2: "red"}

    import matplotlib.patches as mpatches

    fig, axes = plt.subplots(3, 1, figsize=(14, 10), sharex=True)

    axes[0].plot(steps, risks, linewidth=2)
    axes[0].axhline(y=1.0, linestyle="--", alpha=0.5)
    axes[0].fill_between(steps, risks, alpha=0.2)
    axes[0].set_ylabel("Risk Score")
    axes[0].set_title(f"SARSA Episode Timeline — User: {chosen_episode['user'].iloc[0]}")
    axes[0].grid(True, alpha=0.3)

    mal_steps = [i + 1 for i, l in enumerate(labels) if l == 1]
    if mal_steps:
        axes[0].axvspan(min(mal_steps), max(mal_steps), alpha=0.15)

    colors = [action_colors[a] for a in actions]
    axes[1].scatter(steps, actions, c=colors, s=60, zorder=3)
    axes[1].plot(steps, actions, alpha=0.3, linewidth=1)
    axes[1].set_yticks([0, 1, 2])
    axes[1].set_yticklabels(["Close", "Investigate", "Escalate"])
    axes[1].set_ylabel("Action")
    axes[1].grid(True, alpha=0.3)

    if mal_steps:
        axes[1].axvspan(min(mal_steps), max(mal_steps), alpha=0.15)

    pos_rewards = [r if r > 0 else 0 for r in rewards]
    neg_rewards = [r if r < 0 else 0 for r in rewards]
    axes[2].bar(steps, pos_rewards, alpha=0.7, label="Positive reward")
    axes[2].bar(steps, neg_rewards, alpha=0.7, label="Negative reward")
    axes[2].axhline(y=0, linewidth=0.5)
    axes[2].set_ylabel("Reward")
    axes[2].set_xlabel("Step")
    axes[2].legend()
    axes[2].grid(True, alpha=0.3)

    if mal_steps:
        axes[2].axvspan(min(mal_steps), max(mal_steps), alpha=0.15)

    patches = [mpatches.Patch(color=c, label=action_names[a]) for a, c in action_colors.items()]
    axes[1].legend(handles=patches, loc="upper right")

    plt.tight_layout()
    plt.show()

    print(f"\n=== Episode Summary for {chosen_episode['user'].iloc[0]} ===")
    print(f"Total steps: {len(steps)}")
    print(f"Malicious steps: {sum(labels)}")
    print(f"Close decisions: {actions.count(0)} ({actions.count(0)/len(actions)*100:.1f}%)")
    print(f"Investigate decisions: {actions.count(1)} ({actions.count(1)/len(actions)*100:.1f}%)")
    print(f"Escalate decisions: {actions.count(2)} ({actions.count(2)/len(actions)*100:.1f}%)")
    print(f"Total reward: {sum(rewards):.2f}")
    print(f"Correct escalations: {sum(1 for a, l in zip(actions, labels) if a == 2 and l == 1)}")
    print(f"False alarms: {sum(1 for a, l in zip(actions, labels) if a == 2 and l == 0)}")
    print(f"Missed threats: {sum(1 for a, l in zip(actions, labels) if a == 0 and l == 1)}")

